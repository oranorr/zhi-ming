  String _generateRandomChineseString(int minLength, int maxLength) {
    final length = minLength + _random.nextInt(maxLength - minLength + 1);
    String result = '';
    for (int i = 0; i < length; i++) {
      result += _chineseChars[_random.nextInt(_chineseChars.length)];
    }
    return result;
  }

  final List<Map<String, Color>> _colorPairs = [
    {'back': ZColors.blueLight, 'arrow': ZColors.blueMiddle},
    {'back': ZColors.pinkMiddle, 'arrow': ZColors.pinkDark},
    {'back': ZColors.yellowLight, 'arrow': ZColors.yellowMiddle},
    {'back': ZColors.purpleLight, 'arrow': ZColors.purpleMiddle},
  ];

  List<QuestionEntity> get questions {
    List<QuestionEntity> generatedQuestions = [];
    for (int i = 0; i < 20; i++) {
      // Получаем цветовую пару по порядку, циклически повторяя список
      final colorPair = _colorPairs[_colorIndex % _colorPairs.length];
      _colorIndex++; // Переходим к следующему цвету

      generatedQuestions.add(
        QuestionEntity(
          title: _generateRandomChineseString(2, 5),
          subtitle: _generateRandomChineseString(2, 5),
          backColor: colorPair['back']!,
          arrowColor: colorPair['arrow']!,
        ),
      );
    }
    return generatedQuestions;
  }
